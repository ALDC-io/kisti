class _KittWaveform(QWidget):
    """KITT 2.0 voice modulator: 3 columns x 14 horizontal dash segments.

    Supports two modes:
      - Real amplitude: fed from Piper TTS audio envelope via set_amplitude()
      - Random fallback: when no audio player is available
    """

    def __init__(self, parent=None):
        super().__init__(parent)
        self.setMinimumSize(120, 100)
        # Compositorless X11: tell Qt we paint our entire rect every frame.
        # Without this, backing store flush is unreliable without a compositor.
        self.setAttribute(Qt.WA_OpaquePaintEvent)
        self._active = False
        self._levels = [0, 0, 0]
        self._prev_center = 0       # Track previous center for decay
        self._real_amplitude = 0.0  # 0.0-1.0 from audio player
        self._use_real_amplitude = False
        self._intensity = 1.0       # 0.0-1.0 brightness multiplier (dims during pauses)

        self._timer = QTimer(self)
        self._timer.setInterval(50)  # 20 Hz waveform update (was 80ms/12.5Hz)
        self._timer.timeout.connect(self._update_levels)
        self._timer.start()

    def set_active(self, active):
        self._active = active
        if not active:
            self._levels = [0, 0, 0]
            self._real_amplitude = 0.0
            self.repaint()

    def set_amplitude(self, amplitude: float):
        """Set real amplitude from audio playback (0.0-1.0)."""
        self._real_amplitude = max(0.0, min(1.0, amplitude))
        self._use_real_amplitude = True
        # Intensity tracks amplitude — dims during pauses (floor at 0.3)
        if amplitude > 0.1:
            self._intensity = min(1.0, self._intensity + 0.15)  # Ramp up
        else:
            self._intensity = max(0.3, self._intensity - 0.08)  # Fade down

    def _update_levels(self):
        if self._active:
            if self._use_real_amplitude:
                # Drive directly from real audio amplitude — no smoothing
                # This lets syllable-level cadence come through
                amp = self._real_amplitude
                center = int(amp * 7)  # Direct map, no minimum here (decay handles it)
                # Outer bars in lockstep
                outer = max(0, int(center * 0.6))
                left = outer
                right = outer
            else:
                # Fallback: random levels
                center = random.randint(2, 7)
                left = max(0, int(center * random.uniform(0.4, 1.0)))
                right = max(0, int(center * random.uniform(0.4, 1.0)))

            # Decay: during pauses, step down 1 level per tick instead of snapping
            if center < self._prev_center:
                center = self._prev_center - 1  # Gentle step-down, can reach 0
            # Minimum 1 segment while active (but decay can bring it to 1)
            center = max(1, center)
            self._prev_center = center

            # Outer bars: only show when center >= 2, capped at 65% of center.
            if center < 2:
                left = 0
                right = 0
            else:
                left = max(0, min(int(center * 0.65), left))
                right = max(0, min(int(center * 0.65), right))

            self._levels = [left, center, right]
            # Force synchronous paint when actively animating (compositorless X11)
            self.repaint()
        else:
            self._levels = [0, 0, 0]
            self.update()

    def paintEvent(self, event):
        p = QPainter(self)
        p.setRenderHint(QPainter.Antialiasing)
        w, h = self.width(), self.height()

        p.fillRect(0, 0, w, h, QColor(BG_DARK))

        num_segments = 7
        num_cols = 3
        seg_gap = 4           # Increased gap between segments for clarity
        col_gap = 10          # Wider column spacing
        outer_seg_shrink = 1  # Outer columns: segments 1px shorter for crispness

        total_col_width = (w - (num_cols + 1) * col_gap) / num_cols
        seg_h = max(3, (h / 2 - num_segments * seg_gap) / num_segments)
        center_y = h / 2
        base_color = QColor(KISTI_RED)

        # Horizontal gradient: center column = 1.0, outer columns = 0.6
        col_fade = [0.6, 1.0, 0.6]

        for col_idx in range(num_cols):
            level = self._levels[col_idx]
            col_x = col_gap + col_idx * (total_col_width + col_gap)
            h_fade = col_fade[col_idx]  # Horizontal fade factor

            for seg in range(num_segments):
                dist = seg  # 0 = closest to center, 6 = furthest
                lit = seg < level

                # Sakura palette: light pink core → rose → KiSTI red → dark
                # Cherry blossom gradient matching STI logo coloring
                if lit:
                    t = dist / max(1, num_segments - 1)  # 0.0 → 1.0
                    # Deep rose core → crimson → KiSTI Red → Dark
                    if t < 0.3:
                        # Core: warm rose (not pastel — deeper, richer)
                        f = t / 0.3
                        fr = 240
                        fg = int(100 - 60 * f)   # 100 → 40
                        fb = int(120 - 50 * f)   # 120 → 70
                    elif t < 0.6:
                        # Mid: crimson rose to KiSTI red
                        f = (t - 0.3) / 0.3
                        fr = int(230 - 30 * f)   # 230 → 200
                        fg = int(40 - 30 * f)    # 40 → 10
                        fb = int(70 - 19 * f)    # 70 → 51
                    else:
                        # Outer: KiSTI red to dark
                        f = (t - 0.6) / 0.4
                        fr = int(200 - 120 * f)  # 200 → 80
                        fg = int(10 - 10 * f)    # 10 → 0
                        fb = int(51 - 31 * f)    # 51 → 20

                    # Apply horizontal and intensity gradients
                    combined = h_fade * self._intensity
                    r = int(fr * combined)
                    g = int(fg * combined)
                    b = int(fb * combined)
                    opacity = max(0.2, combined * (1.0 - t * 0.5))
                else:
                    r, g, b = 20, 5, 0
                    opacity = 0.04

                seg_color = QColor(r, g, b)
                seg_color.setAlphaF(opacity)

                # Glow on inner segments when lit
                if lit and dist < 3:
                    glow_alpha = max(0.0, 0.35 - dist * 0.12)
                    glow_color = QColor(base_color)
                    glow_color.setAlphaF(glow_alpha)
                    glow_expand = 2
                    gy_top = center_y - (seg + 1) * (seg_h + seg_gap) - glow_expand
                    p.setPen(Qt.NoPen)
                    p.setBrush(glow_color)
                    p.drawRoundedRect(
                        QRectF(col_x - glow_expand, gy_top,
                               total_col_width + 2 * glow_expand,
                               seg_h + 2 * glow_expand), 2, 2)
                    gy_bot = center_y + seg * (seg_h + seg_gap) - glow_expand
                    p.drawRoundedRect(
                        QRectF(col_x - glow_expand, gy_bot,
                               total_col_width + 2 * glow_expand,
                               seg_h + 2 * glow_expand), 2, 2)

                # Outer columns: slightly thinner segments for crispness
                is_outer = (col_idx != 1)
                sh = seg_h - outer_seg_shrink if is_outer else seg_h
                sw = total_col_width - (2 if is_outer else 0)
                sx = col_x + (1 if is_outer else 0)

                p.setPen(Qt.NoPen)
                p.setBrush(seg_color)
                y_top = center_y - (seg + 1) * (seg_h + seg_gap)
                p.drawRoundedRect(QRectF(sx, y_top, sw, sh), 1, 1)
                y_bot = center_y + seg * (seg_h + seg_gap)
                p.drawRoundedRect(QRectF(sx, y_bot, sw, sh), 1, 1)

        p.end()


class _ScanBar(QWidget):
    """KITT scanning light bar — always sweeping."""

    def __init__(self, parent=None):
        super().__init__(parent)
        self.setFixedHeight(4)
        self.setAttribute(Qt.WA_OpaquePaintEvent)
        self._pos = 0.0
        self._direction = 1
        self._active = True

        self._timer = QTimer(self)
        self._timer.setInterval(30)
        self._timer.timeout.connect(self._tick)
        self._timer.start()

    def set_active(self, active):
        self._active = active
        self.repaint()

    def _tick(self):
        if self._active:
            self._pos += 0.03 * self._direction
            if self._pos >= 1.0:
                self._direction = -1
            elif self._pos <= 0.0:
                self._direction = 1
            self.update()

    def paintEvent(self, event):
        p = QPainter(self)
        w, h = self.width(), self.height()
        p.fillRect(0, 0, w, h, QColor(BG_DARK))
        if self._active:
            center_x = self._pos * w
            grad = QLinearGradient(center_x - 40, 0, center_x + 40, 0)
            grad.setColorAt(0.0, QColor(0, 0, 0, 0))
            grad.setColorAt(0.5, QColor(KISTI_RED))
            grad.setColorAt(1.0, QColor(0, 0, 0, 0))
            p.fillRect(0, 0, w, h, grad)
        else:
            p.fillRect(0, 0, w, h, QColor(KISTI_RED + "15"))
        p.end()


