    def _detect_hardware(self):
        """Startup: scan hardware, only speak problems. Visual log for details."""
        import subprocess
        import os
        from pathlib import Path
        import time

        issues = []

        # CPU temp
        cpu_temp = None
        try:
            for tz in sorted(Path("/sys/class/thermal/").glob("thermal_zone*")):
                temp_file = tz / "temp"
                if temp_file.exists():
                    cpu_temp = int(temp_file.read_text().strip()) / 1000.0
                    break
        except Exception:
            pass
        if cpu_temp and cpu_temp >= 80:
            issues.append(("critical", f"CPU overheating. {cpu_temp:.0f} degrees."))

        # GPU
        gpu_ok = False
        try:
            result = subprocess.run(["nvidia-smi"], capture_output=True, timeout=5)
            gpu_ok = result.returncode == 0
        except Exception:
            pass
        if not gpu_ok:
            issues.append(("alert", "GPU not detected."))

        # ECU
        ecu_ok = False
        try:
            import can as python_can
            bus = python_can.Bus(interface="socketcan", channel="can0", receive_own_messages=False)
            bus.shutdown()
            ecu_ok = True
        except Exception:
            pass
        if ecu_ok:
            issues.append(("normal", "Ignition detected. All systems nominal."))
        else:
            issues.append(("normal", "No ECU. Standing by."))

        # Ollama
        ollama_ok = False
        try:
            import urllib.request
            import json
            with urllib.request.urlopen("http://localhost:11434/api/tags", timeout=3) as resp:
                models = json.loads(resp.read()).get("models", [])
                ollama_ok = len(models) > 0
        except Exception:
            pass
        if not ollama_ok:
            klog.info("Ollama not running — persona-first mode, no LLM fallback")

        # Ambient weather — read from bridge (populated by YoctopuceReader at 1Hz)
        # Wait up to 3s for the first sensor reading to arrive
        ambient = None
        if self._bridge:
            for _ in range(6):
                snap = self._bridge.snapshot()
                if snap.ambient_available:
                    ambient = {
                        "temp_c": snap.ambient_temp_c,
                        "humidity_pct": snap.ambient_humidity_pct,
                        "pressure_hpa": snap.ambient_pressure_hpa,
                        "dew_point_c": snap.dew_point_c,
                        "density_altitude_ft": snap.density_altitude_ft,
                    }
                    break
                time.sleep(0.5)

        # Visual-only: log all status silently
        self._transcript.append(f"  CPU: {cpu_temp:.0f}°C" if cpu_temp else "  CPU: ---")
        self._transcript.append(f"  GPU: {'OK' if gpu_ok else '---'}")
        self._transcript.append(f"  ECU: {'OK' if ecu_ok else '---'}")
        self._transcript.append(f"  AI: {'OK' if ollama_ok else '---'}")
        if ambient:
            self._transcript.append(f"  Ambient: {ambient['temp_c']:.1f}°C  {ambient['humidity_pct']:.0f}%RH  {ambient['pressure_hpa']:.0f}hPa")
        else:
            self._transcript.append("  Ambient: ---")
        self._request_display_update.emit()

        time.sleep(1)

        # Build a single startup line — KiSTI speaks once, then listens.
        # Previously this was 4+ sequential lines with 2-3s gaps, blocking
        # the mic for ~20s. One consolidated line = ~3-5s, then ready.
        parts = ["Online."]

        # Critical issues first
        critical = [msg for urg, msg in issues if urg == "critical"]
        if critical:
            parts.extend(critical)

        # Status summary
        normal = [msg for urg, msg in issues if urg != "critical"]
        if normal:
            parts.append(normal[-1])  # Last normal issue (e.g. "No ECU. Standing by.")

        # Driving conditions
        if ambient:
            t = ambient["temp_c"]
            h = ambient["humidity_pct"]
            dp = ambient["dew_point_c"]
            da = ambient["density_altitude_ft"]

            if dp > (t - 2):
                status = "damp, reduced grip"
            elif t < 5:
                status = "cold, low grip"
            elif t < 8:
                status = "cool, grip building"
            elif h > 80:
                status = "humid, watch for slick"
            elif t > 35:
                status = "hot, watch tire temps"
            else:
                status = "good" if t < 25 else "warm, good grip"

            if da > 4000:
                status += ", thin air"
            elif da > 3000:
                status += ", high altitude"

            parts.append(f"Conditions {status}.")

        time.sleep(1)
        max_urgency = "critical" if critical else "normal"
        self._queue_lines([" ".join(parts)], urgency=max_urgency)

    def _check_say_file(self):
        """Check for externally injected speech or questions.

        /tmp/kisti_say.txt — speak this text directly (prefix ALERT_/CRITICAL_ for urgency)
        /tmp/kisti_ask.txt — ask KiSTI a question, she thinks via Ollama and responds
        """
        import os
        try:
            # Direct speech
            if os.path.exists(self._say_file):
                with open(self._say_file, "r") as f:
                    text = f.read().strip()
                os.remove(self._say_file)
                if text:
                    urgency = "normal"
                    if text.startswith("CRITICAL_"):
                        urgency = "critical"
                        text = text[9:]
                    elif text.startswith("ALERT_"):
                        urgency = "alert"
                        text = text[6:]
                    self._queue_lines([text], urgency=urgency)

            # Question → LLM → response
            ask_file = "/tmp/kisti_ask.txt"
            if os.path.exists(ask_file):
                with open(ask_file, "r") as f:
                    question = f.read().strip()
                os.remove(ask_file)
                if question:
                    # Show the question in transcript
                    self._transcript.append(f"> {question}")
                    self._update_display()
                    # Process through LLM in background
                    import threading
                    threading.Thread(
                        target=self._process_question,
                        args=(question,),
                        daemon=True,
                    ).start()
        except Exception:
            pass

    def _init_usb_baseline(self):
        """Capture initial USB devices so we only announce changes."""
        self._known_usb = self._scan_usb_devices()

    def _scan_usb_devices(self) -> set:
        """Scan connected USB devices, return set of (vendor, product) names."""
        import subprocess
        devices = set()
        try:
            result = subprocess.run(
                ["lsusb"], capture_output=True, text=True, timeout=5,
            )
            for line in result.stdout.strip().split("\n"):
                if "ID " in line:
                    # e.g. "Bus 001 Device 003: ID 1234:5678 Some Device Name"
                    parts = line.split("ID ", 1)
                    if len(parts) > 1:
                        dev_info = parts[1].strip()
                        # Skip hubs and root hubs
                        if "hub" not in dev_info.lower():
                            devices.add(dev_info)
        except Exception:
            pass
        return devices

    def _check_usb_devices(self):
        """Detect USB device changes and announce them."""
        current = self._scan_usb_devices()
        added = current - self._known_usb
        removed = self._known_usb - current

        for dev in added:
            name = dev.split(" ", 1)[1] if " " in dev else dev
            self._transcript.append(f"  [USB+] {name}")
            self._queue_lines([f"New device detected: {name}"])
            klog.info("USB connected: %s", name)

        for dev in removed:
            name = dev.split(" ", 1)[1] if " " in dev else dev
            self._transcript.append(f"  [USB-] {name}")
            self._queue_lines([f"Device disconnected: {name}"], urgency="alert")
            klog.info("USB disconnected: %s", name)

        if added or removed:
            self._update_display()

        self._known_usb = current

    def _process_question(self, question: str):
        """Background: send question to Ollama LLM, queue the response."""
        try:
            from voice.llm_engine import LLMEngine
            if not hasattr(self, '_llm'):
                self._llm = LLMEngine()
                self._llm.start()

            # Build telemetry context from Jetson CPU temp
            context = ""
            try:
                from pathlib import Path
                for tz in sorted(Path("/sys/class/thermal/").glob("thermal_zone*")):
                    temp_file = tz / "temp"
                    if temp_file.exists():
                        cpu_temp = int(temp_file.read_text().strip()) / 1000.0
                        context = f"Jetson CPU: {cpu_temp:.1f}°C. No ECU connected."
                        break
            except Exception:
                context = "No ECU connected. Running on Jetson Orin Nano."

            response = self._llm.query(
                user_message=question,
                telemetry_context=context,
                si_drive_mode="Intelligent",
            )
            klog.info("LLM response [%s]: %s", response.tier, response.text[:80])
            self._queue_lines([response.text])
        except Exception as exc:
            klog.warning("LLM query failed: %s", exc)
            self._queue_lines(["I couldn't process that. Try again."])

