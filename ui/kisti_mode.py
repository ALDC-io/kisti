    def _start_speaking(self, text, urgency: str = "normal"):
        """Begin typewriter output of a line + audio playback if available."""
        self._speaking = True
        self._char_queue = list(text)
        self._current_line = ""

        # Trigger real audio playback if Piper is available
        # AudioPlayer queues internally if busy — speech is never dropped
        if self._voice_enabled and self._audio_player and self._audio_player.is_available:
            klog.info("_start_speaking [%s]: %s", urgency, text[:50])
            self._audio_player.speak(text, urgency=urgency)
            # Hold typewriter until playback_started signal fires (see _on_audio_started)
            self._pause_ticks = 9999  # Will be released by _on_audio_started
        else:
            # No audio available — typewriter only, no waveform
            pass

    def _stop_speaking(self):
        """End speech — commit line to transcript. Scanner keeps sweeping."""
        self._speaking = False
        self._waveform.set_active(False)
        if self._current_line.strip():
            self._transcript.append(self._current_line)
        self._current_line = ""
        self._idle_timer_s = 0
        self._type_timer.setInterval(_CHAR_MS)  # Reset to default speed
        self._update_display()

    def _type_tick(self):
        """Called every _CHAR_MS — type one character or advance to next line."""
        if self._pause_ticks > 0:
            self._pause_ticks -= 1
            return

        if self._speaking and self._char_queue:
            # Type next character
            ch = self._char_queue.pop(0)
            self._current_line += ch
            self._update_display()
        elif self._speaking and not self._char_queue:
            if self._audio_playing:
                # Typewriter finished but audio still playing — wait for audio
                return
            # Both typewriter and audio done — commit line
            self._stop_speaking()
            # Pause between lines (500ms = ~17 ticks at 30ms)
            self._pause_ticks = int(500 / _CHAR_MS)
        elif not self._speaking and not self._audio_playing and self._line_queue:
            # Both idle — start next queued line
            item = self._line_queue.pop(0)
            if isinstance(item, tuple):
                next_line, urgency = item
            else:
                next_line, urgency = item, "normal"
            if next_line.strip():
                self._start_speaking(next_line, urgency)
            else:
                # Empty line = pause (~1s)
                self._transcript.append("")
                self._pause_ticks = int(1000 / _CHAR_MS)
                self._update_display()

